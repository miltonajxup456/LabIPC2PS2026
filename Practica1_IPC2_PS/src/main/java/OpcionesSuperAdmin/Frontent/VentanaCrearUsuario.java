    private void escribirRol(String rol) {
        espacioRol.setText(rol);
    }
    
    public void agregarSucursal(PlantillaSucursal plantilla) {
        espacioSucursales.add(plantilla);
        actualizarSucursales();
    }
    
    public void agregarSucursalElegida() {
        espacioSucursalElegida.add(plantilla);
        actualizarSucursales();
        actualizarElegida();
    }
    
    public void quitarEleccion() {
        agregarSucursal(plantilla);
        actualizarElegida();
        plantilla = null;
        sucursal = null;
    }
    
    private void actualizarElegida() {
        espacioSucursalElegida.revalidate();
        espacioSucursalElegida.repaint();
    }
    
    private void actualizarSucursales() {
        espacioSucursales.revalidate();
        espacioSucursales.repaint();
    }
    
    private void limpiarSucursales() {
        espacioSucursales.removeAll();
        actualizarSucursales();
        espacioSucursalElegida.removeAll();
        actualizarElegida();
        espacioNombre.setText("");
        espacioContraseña.setText("");
        escribirRol("Ningun Rol");
        rolElegido = 0;
        plantilla = null;
        sucursal = null;
    }
    
