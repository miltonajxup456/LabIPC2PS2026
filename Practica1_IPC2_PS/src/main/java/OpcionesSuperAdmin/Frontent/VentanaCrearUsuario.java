    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreUsuario = espacioNombre.getText();
        String contraseña = espacioContraseña.getText();
        if (ValidarFrontent.estaVacio(nombreUsuario) || ValidarFrontent.estaVacio(contraseña)) {
            JOptionPane.showMessageDialog(this, "No se puede dejar el espacio vacio");
            return;
        }
        if (!ValidarFrontent.revisionValido(nombreUsuario, 40)) {
            JOptionPane.showMessageDialog(this, "El nombre no puede sobrepasar los 50 caracteres");
            return;
        }
        if (!ValidarFrontent.revisionValido(contraseña, 15)) {
            JOptionPane.showMessageDialog(this, "La constraseña no puede tener mas de 20 caracteres");
            return;
        }
        if (rolElegido <= 0) {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado un rol");
            return;
        }
        if (sucursal == null) {
            JOptionPane.showMessageDialog(this, "No se ha escogido una sucursal");
            return;
        }
        try {
            gestorUsuario.agregarUsuario(nombreUsuario, contraseña, rolElegido, sucursal.getIdSucursal());
            JOptionPane.showMessageDialog(this, "Se ha creado el Usuario "+nombreUsuario+" con exito");
            espacioNombre.setText("");
            espacioContraseña.setText("");
            escribirRol("Ningun Rol");
            rolElegido = 0;
            quitarEleccion();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

