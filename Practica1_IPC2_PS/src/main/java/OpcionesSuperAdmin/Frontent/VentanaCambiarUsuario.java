    public void agregarUsuario(PlantillaUsuarioCambio plantilla) {
        espacioUsuarios.add(plantilla);
        actualizarUsuarios();
    }
    
    public void agregarUsuarioElegido(PlantillaUsuarioCambio plantilla) {
        espacioUsuarioElegido.add(plantilla);
        actualizarUsuarios();
        actualizarUsuarioElegido();
    } 
    
    public void quitarUsuarioElegido(PlantillaUsuarioCambio plantilla) {
        agregarUsuario(plantilla);
        actualizarUsuarioElegido();
        plantilllaUsuario = null;
        usuario = null;
    }
    
    public void agregarSucursal(PlantillaSucursalCambio plantilla) {
        espacioSucursales.add(plantilla);
        actualizarSucursales();
    }
    
    public void agregarSucursalElegida(PlantillaSucursalCambio plantilla) {
        espacioSucursalElegida.add(plantilla);
        actualizarSucursales();
        actualizarSucursalElegida();
    }
    
    public void quitarSucursalElegida() {
        agregarSucursal(plantillaSucursal);
        actualizarSucursalElegida();
        plantillaSucursal = null;
        sucursal = null;
    }
    
    private void actualizarUsuarios() {
        espacioUsuarios.revalidate();
        espacioUsuarios.repaint();
    }
    
    private void actualizarUsuarioElegido() {
        espacioUsuarioElegido.revalidate();
        espacioUsuarioElegido.repaint();
    }
    
    private void actualizarSucursales() {
        espacioSucursales.revalidate();
        espacioSucursales.repaint();
    }
    
    private void actualizarSucursalElegida() {
        espacioSucursalElegida.revalidate();
        espacioSucursalElegida.repaint();
    }
    
    private void limpiarPaneles() {
        espacioUsuarios.removeAll();
        actualizarUsuarios();
        espacioUsuarioElegido.removeAll();
        actualizarUsuarioElegido();
        espacioSucursales.removeAll();
        actualizarSucursales();
        espacioSucursalElegida.removeAll();
        actualizarSucursalElegida();
        plantilllaUsuario = null;
        usuario = null;
        plantillaSucursal = null;
        sucursal = null;
    }
    
