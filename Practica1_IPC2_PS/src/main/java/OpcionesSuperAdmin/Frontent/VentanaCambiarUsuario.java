    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (usuario == null) {
            JOptionPane.showMessageDialog(this, "No se ha escogido al Usuario");
            return;
        }
        if (sucursal == null) {
            JOptionPane.showMessageDialog(this, "No se ha escogido una sucursal");
            return;
        }
        try {
            gestorUsuario.cambiarDeSucursal(sucursal.getIdSucursal(), usuario.getIdUsuario());
            JOptionPane.showMessageDialog(this, "Se ha cambiado a "+usuario.getNombre()+" a la sucursal "+sucursal.getNombre());
            quitarUsuarioElegido(plantilllaUsuario);
            quitarSucursalElegida();
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());  
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
